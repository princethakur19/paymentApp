pet = "dog"
age = 2

if pet == "dog":
    if age < 3:
        print("puppy food")
    elif age <= 6:
        print("adult dog food")
    else:
        print("Senior dog food")

elif pet == "cat":
    if age < 3:
        print("kitten food")
    elif age <= 5:
        print("adult cat food")
    else:
        print("Senior cat food")